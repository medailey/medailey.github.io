/* Four queries joined by UNION statements.  Each query returns AXIS, CHART, and QUANTITY 
in order to populate the radar charts */

/* Jobs Housing Balance */
SELECT 'Jobs Housing Balance' AS AXIS, SUM(HSHLD)/SUM(EMP) AS QUANTITY, ACTCENTER AS CHART
FROM ZONEDATA, ACTCENTERTOZONE
WHERE ZONEDATA.ZONE = ACTCENTERTOZONE.ZONE
GROUP BY ACTCENTER

UNION

/* Accessible Employment */
SELECT 'Accessible Employment' AS AXIS, SUM(EMP) AS QUANTITY, ACTCENTER AS CHART
FROM ZONEDATA,
	(SELECT DISTINCT ORIG_TAZ, ACTCENTER FROM 
	  (SELECT DISTINCT ORIG_TAZ, DEST_TAZ, MIN(TRAVEL_TIME) AS TRAVELTIME FROM TRIPS
     GROUP BY ORIG_TAZ, DEST_TAZ) AS TRAVELTIMES, ACTCENTER
	WHERE TRAVELTIME <= 30 AND TRAVELTIME > 0 AND DEST_TAZ = ACTCENTER.CENTERZONE)
	AS TEMP
WHERE ZONEDATA.ZONE=TEMP.ORIG_TAZ
GROUP BY ACTCENTER

UNION

/* Transit mode share */
SELECT 'Transit Mode Share' AS AXIS, SUM(TRANSIT_TRIPS)/SUM(TOTAL_TRIPS*1.0) AS QUANTITY, ACTCENTER AS CHART
FROM ACTCENTERTOZONE,
  (SELECT HOME_TAZ, COUNT(TRIP_MODE_NAME) AS TOTAL_TRIPS 
  FROM TRIPS 
  GROUP BY HOME_TAZ) AS TEMP,
  (SELECT HOME_TAZ, COUNT(TRIP_MODE_NAME) AS TRANSIT_TRIPS 
  FROM TRIPS
  WHERE TRIP_MODE_NAME IN ('WALK_ALLTRN','WALK_PRMTRN','PNR_ALLTRN','PNR_PRMTRN','KNR_ALLTRN','KNR_PRMTRN')
  GROUP BY HOME_TAZ) AS TEMP2
WHERE TEMP.HOME_TAZ=TEMP2.HOME_TAZ AND TEMP.HOME_TAZ=ACTCENTERTOZONE.ZONE
GROUP BY ACTCENTER

UNION

/* Zero Car Transit Trips per HH */
SELECT  'Zero Car Transit Trips Per HH' AS AXIS, (ZERO_CAR_TRANSIT_TRIPS / HHS) AS QUANTITY, TEMP.ACTCENTER AS CHART
FROM
  (SELECT COUNT(TRIP_MODE_NAME) AS ZERO_CAR_TRANSIT_TRIPS, ACTCENTER
  FROM TRIPS, ACTCENTERTOZONE
  WHERE ACTCENTERTOZONE.ZONE=TRIPS.ORIG_TAZ AND TRIPS.HH_AUTOS=0 AND TRIP_MODE_NAME IN ('WALK_ALLTRN','WALK_PRMTRN','PNR_ALLTRN','PNR_PRMTRN','KNR_ALLTRN','KNR_PRMTRN')
  GROUP BY ACTCENTER) AS TEMP, 
  
  (SELECT SUM(HSHLD) AS HHS, ACTCENTER
  FROM ZONEDATA, ACTCENTERTOZONE
  WHERE ACTCENTERTOZONE.ZONE=ZONEDATA.ZONE
  GROUP BY ACTCENTER) AS TEMP2
  
WHERE TEMP.ACTCENTER=TEMP2.ACTCENTER
